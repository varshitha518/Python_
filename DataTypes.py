# Integer
integer_variable = 10
print("Integer Variable:", integer_variable, type(integer_variable))

# Float
float_variable = 3.14
print("Float Variable:", float_variable, type(float_variable))

# String
string_variable = "Hello, World!"
print("String Variable:", string_variable, type(string_variable))

# Boolean
boolean_variable = True
print("Boolean Variable:", boolean_variable, type(boolean_variable))

# List
list_variable = [1, 2, 3, 4, 5]
print("List Variable:", list_variable, type(list_variable))

# Tuple
tuple_variable = (1, 2, 3, 4, 5)
print("Tuple Variable:", tuple_variable, type(tuple_variable))

# Dictionary
dictionary_variable = {"apple": 2, "banana": 3, "cherry": 5}
print("Dictionary Variable:", dictionary_variable, type(dictionary_variable))

# Set
set_variable = {1, 2, 3, 4, 5}
print("Set Variable:", set_variable, type(set_variable))

# None
none_variable = None
print("None Variable:", none_variable, type(none_variable))
